#include <iostream>
using namespace std;


// here is the 10*10 pattern is prined on the output screen 
int main(){
    // outer loop
    for (int i=0 ; i< 10 ;i++){
        //inner loop
        for (int j = 0 ; j< 10 ; j++){
            cout << "*";
        }
        cout << endl;
    }
}
